interface ToothMeshProps {
  geometry: THREE.BufferGeometry;
  toothId: number;
  isSelected: boolean;
  onSelect?: (id: number, worldPos: THREE.Vector3) => void;
  conditionColor?: string;
}

function ToothMesh({ geometry, toothId, isSelected, onSelect, conditionColor }: ToothMeshProps) {
  const meshRef = useRef<THREE.Mesh>(null);
  const [hovered, setHovered] = useState(false);

  const handleClick = useCallback(
    (e: ThreeEvent<MouseEvent>) => {
      e.stopPropagation();
      // Get the world-space center of this tooth
      const mesh = meshRef.current;
      if (mesh) {
        const box = new THREE.Box3().setFromObject(mesh);
        const center = new THREE.Vector3();
        box.getCenter(center);
        onSelect?.(toothId, center);
      } else {
        onSelect?.(toothId, e.point);
      }
    },
    [onSelect, toothId],
  );

  const handlePointerOver = useCallback((e: ThreeEvent<PointerEvent>) => {
    e.stopPropagation();
    setHovered(true);
    document.body.style.cursor = "pointer";
  }, []);

  const handlePointerOut = useCallback(() => {
    setHovered(false);
    document.body.style.cursor = "default";
  }, []);

  // If tooth has a condition, use that color; otherwise default holographic blue
  const hasCondition = !!conditionColor;
  const isMissing = conditionColor === "#6B7280";
  const baseColor = conditionColor || "#7cc4f0";
  const baseEmissive = conditionColor || "#5ab0e8";

  const emissiveIntensity = isSelected ? 1.4 : hovered ? 1.0 : isMissing ? 0.2 : hasCondition ? 0.9 : 0.7;
  const color = isSelected ? "#88eeff" : hovered ? baseColor : baseColor;
  const emissive = isSelected ? "#66eeff" : hovered ? baseEmissive : baseEmissive;
  const opacity = isSelected ? 0.9 : hovered ? 0.8 : isMissing ? 0.2 : hasCondition ? 0.75 : 0.6;

  return (
    <mesh
      ref={meshRef}
      geometry={geometry}
      onClick={handleClick}
      onPointerOver={handlePointerOver}
      onPointerOut={handlePointerOut}
    >
      <meshPhysicalMaterial
        color={color}
        emissive={emissive}
        emissiveIntensity={emissiveIntensity}
        transparent
        opacity={opacity}
        roughness={0.1}
        metalness={0.9}
        clearcoat={1}
        clearcoatRoughness={0.05}
        side={THREE.DoubleSide}
      />
    </mesh>
  );
}

