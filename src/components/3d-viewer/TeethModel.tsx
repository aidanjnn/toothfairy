/**
 * Assigns US Universal tooth numbers (1-32) to geometry islands
 * based on their spatial position (bounding box center).
 *
 * Universal numbering:
 *   Upper right 1-8 (right to left from patient perspective)
 *   Upper left 9-16
 *   Lower left 17-24
 *   Lower right 25-32
 *
 * The model is rotated -PI/2 on X, so in local model space:
 *   X = left/right, Y = front/back, Z = up/down (upper vs lower jaw)
 */
function assignUniversalNumbers(
  islands: THREE.BufferGeometry[],
): { geo: THREE.BufferGeometry; toothNum: number }[] {
  // Compute center for each island
  const withCenters = islands.map((geo) => {
    geo.computeBoundingBox();
    const center = new THREE.Vector3();
    geo.boundingBox!.getCenter(center);
    return { geo, center };
  });

  // Split into upper (z > median z) and lower jaw
  const allZ = withCenters.map((i) => i.center.z);
  const medianZ = allZ.sort((a, b) => a - b)[Math.floor(allZ.length / 2)];

  const upper = withCenters.filter((i) => i.center.z >= medianZ);
  const lower = withCenters.filter((i) => i.center.z < medianZ);

  // Upper: sort right to left (descending X = patient's right first)
  // Universal 1 = upper right third molar, 16 = upper left third molar
  upper.sort((a, b) => b.center.x - a.center.x);

  // Lower: sort left to right (ascending X = patient's left first)
  // Universal 17 = lower left third molar, 32 = lower right third molar
  lower.sort((a, b) => a.center.x - b.center.x);

  const result: { geo: THREE.BufferGeometry; toothNum: number }[] = [];

  // Map upper islands to 1..16
  const upperStep = upper.length > 0 ? 16 / upper.length : 1;
  upper.forEach((item, i) => {
    const num = Math.round(1 + i * upperStep);
    result.push({ geo: item.geo, toothNum: Math.min(num, 16) });
  });

  // Map lower islands to 17..32
  const lowerStep = lower.length > 0 ? 16 / lower.length : 1;
  lower.forEach((item, i) => {
    const num = Math.round(17 + i * lowerStep);
    result.push({ geo: item.geo, toothNum: Math.min(num, 32) });
  });

  return result;
}

